export const replaceKeywordsWithEnName = (text: string): string => {
  for (const member of vspo.members) {
    for (const keyword of member.searchKeywords) {
      if (text.includes(keyword)) {
        text = text.replace(new RegExp(keyword, "g"), member.enName);
      }
    }
  }
  return text;
};
